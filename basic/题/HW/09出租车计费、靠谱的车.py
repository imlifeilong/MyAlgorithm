def main(n):
    """
    思路，个位数里去掉4，只剩9个数，就是9进制的累计 依次类推 十位数去掉40 百位数去掉 400
    """
    res = 0
    i = 0
    while n > 0:
        d = n % 10  # 个位

        n //= 10  # 除以10 判断十位数上的数字
        if d > 4:  # 当前数大于4 就减1
            d -= 1
        # print(d, n, i)
        tmp = pow(9, i) * d  # i=0的时候 处理个位数，i=1的时候处理十位数 依次类推
        res += tmp
        i += 1
    print(res)


n = 100
n = 17
n = 5
main(n)
